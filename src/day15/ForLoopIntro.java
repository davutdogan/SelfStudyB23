package day15;

public class ForLoopIntro {
    public static void main(String[] args) {

        for (int i = 0; i <=4 ; i++) {
            System.out.print("Hello, ");
        }

        System.out.println();

        for (int i = 1; i <=5 ; i++) {
            System.out.println("Hello                       Hello");

        }


        for (int i = 1; i <=5 ; i++) {
            System.out.print("Hello, ");
        }

        System.out.println();
    }
}
